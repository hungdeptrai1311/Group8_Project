package Model;

import java.sql.Date;

/**
 *
 * @author vuman
 */
public class Bill {
    private int billId;
    private int cusId;
    private Date date;
    private String address;

    public Bill() {
    }

    public Bill(int cusId, Date date, String address) {
        this.cusId = cusId;
        this.date = date;
        this.address = address;
    }

    public int getBillId() {
        return billId;
    }

    public void setBillId(int billId) {
        this.billId = billId;
    }

    public int getCusId() {
        return cusId;
    }

    public void setCusId(int cusId) {
        this.cusId = cusId;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
    
}
